package beans;

public class NombreDoc {
	private String nbrDoc;
	private String nbrCour;
	private String nbrExam;
	public String getNbrDoc() {
		return nbrDoc;
	}
	public void setNbrDoc(String nbrDoc) {
		this.nbrDoc = nbrDoc;
	}
	public String getNbrCour() {
		return nbrCour;
	}
	public void setNbrCour(String nbrCour) {
		this.nbrCour = nbrCour;
	}
	public String getNbrExam() {
		return nbrExam;
	}
	public void setNbrExam(String nbrExam) {
		this.nbrExam = nbrExam;
	}

}
