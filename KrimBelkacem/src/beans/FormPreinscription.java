package beans;

public class FormPreinscription {
	private String nom;
	private String prenom;
	private String date_nais;
	private String lieu_Nais;
	private String adresse;
	public String getNom() {
		return nom;
	}
	public void setNom(String nom) {
		this.nom = nom;
	}
	public String getPrenom() {
		return prenom;
	}
	public void setPrenom(String prenom) {
		this.prenom = prenom;
	}
	public String getDate_nais() {
		return date_nais;
	}
	public void setDate_nais(String date_nais) {
		this.date_nais = date_nais;
	}
	public String getLieu_nais() {
		return lieu_Nais;
	}
	public void setLieu_nais(String lieuDeNais) {
		this.lieu_Nais = lieuDeNais;
	}
	public String getAdresse() {
		return adresse;
	}
	public void setAdresse(String adresse) {
		this.adresse = adresse;
	}
	

}
